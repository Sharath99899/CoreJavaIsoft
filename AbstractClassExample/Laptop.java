package com.AbstractClassExample;

/*
 * An Abstarct class is a class which can contain the abstarct methods and is knows as abstarct Class.
 * Cannot be instantiated (Objects cannot be created directly).
 * Can have both abstract (unimplemented) and concrete (implemented) methods.
 * Declared using the abstract keyword.
 */
public interface Laptop {
	
	public void copy();
	public void cut();
	public void past();
	public void keyboard();

}
